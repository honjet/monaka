{-# LANGUAGE FlexibleInstances #-}
{-# LANGUAGE OverloadedStrings #-}

module Monaka.Markov
  ( markovStr
  , markovNodes
  ) where

import           Control.Monad
import qualified Data.MarkovChain as Markov
import qualified Data.Text        as T
import           System.Random
import           Text.MeCab

instance Ord (Node T.Text) where
  compare x y = compare (nodeFeature x) (nodeFeature y)

markovStr :: T.Text -> IO T.Text
markovStr source = do
  nodes <- markovNodes source
  let text = map nodeSurface nodes
  return $ T.concat $ map nodeSurface nodes

markovNodes :: T.Text -> IO [Node T.Text]
markovNodes source = do
  mecab <- new ["mecab", "-l0"]
  nodes <- parseToNodes mecab source
  let nodes' = [x | x <- nodes, all (`notElem` (T.unpack $ nodeSurface x)) ignoreChars, ((NOR ==).nodeStat) x]
  rnd <- randomRIO (0, length nodes')
  gen <- newStdGen
  return $ take 500 $ Markov.run 3 nodes' rnd gen

